void abrirllave() {
 
  // 1. Cambiar a layout US (Win + Espacio)
  Keyboard.press(KEY_LEFT_GUI);     // Win
  Keyboard.press(' ');              // Espacio
  delay(200);
  Keyboard.releaseAll();
  delay(500);                       // Espera a que el sistema cambie el layout

  // 2. Escribir `{` con Shift + [ (válido en layout US)
  Keyboard.press(KEY_LEFT_SHIFT);
  Keyboard.press('[');
  delay(100);
  Keyboard.releaseAll();
  delay(300);

  // 3. Volver a layout Latinoamericano (Win + Espacio otra vez)
  Keyboard.press(KEY_LEFT_GUI);     // Win
  Keyboard.press(' ');
  delay(200);
  Keyboard.releaseAll();
  delay(500);                       // Espera a que el sistema regrese al layout original
}

