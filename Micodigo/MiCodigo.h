void setup() {
  pinMode(joyBtn, INPUT_PULLUP);

  display.begin(SSD1306_SWITCHCAPVCC, 0x3C);
  display.clearDisplay();

  display.drawBitmap(0, 0, epd_bitmap, 128, 64, SSD1306_WHITE);
  display.display();
  delay(2500);

  Keyboard.begin();
  drawMenu();
}

// -------------------------
//        LOOP
// -------------------------
void loop() {
  int yVal = analogRead(joyY);
  bool currentBtnState = digitalRead(joyBtn);

  // Navegación del menú
  if (millis() - lastMove > 300) {
    if (yVal < 400 && selected > 0) {
      selected--;
      drawMenu();
      lastMove = millis();
    } 
    else if (yVal > 600 && selected < 4) {
      selected++;
      drawMenu();
      lastMove = millis();
    }
  }

  // Botón para seleccionar
  if (lastBtnState == HIGH && currentBtnState == LOW) {
    executeOption(selected);
    delay(300);
  }

  lastBtnState = currentBtnState;
}